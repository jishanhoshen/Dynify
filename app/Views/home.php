  // AJAX form submission
  $('#contactForm').on('submit', function(e) {
    e.preventDefault();
    var formData = {
      name: $('#name').val().trim(),
      email: $('#email').val().trim(),
      phone: $('#phone').val().trim(),
      message: $('#message').val().trim()
    };

    toastr.clear();

    // Form validation
    var isValid = true;

    if (formData.name === "" || formData.email === "" || formData.phone === "" || formData.message === "") {
      toastr.error('All fields are required');
      isValid = false;
    }

    // Check if name is not empty
    if (formData.name === "") {
      // toastr.error('Name is required');
      $('#name').addClass('is-invalid');
      isValid = false;
    } else if (formData.name.length < 3 || formData.name.length > 50) {
      toastr.error('Name must be between 3 and 50 characters');
      $('#name').addClass('is-invalid');
      isValid = false;
    } else {
      $('#name').removeClass('is-invalid');
    }

    // Check if email is not empty and valid
    if (formData.email === "") {
      // toastr.error('Email is required');
      $('#email').addClass('is-invalid');
      isValid = false;
    } else if (!validateEmail(formData.email)) {
      toastr.error('Enter a valid email');
      isValid = false;
    } else {
      $('#email').removeClass('is-invalid');
    }

    // Check if phone is not empty and valid
    if (formData.phone === "") {
      // toastr.error('Phone is required');
      $('#phone').addClass('is-invalid');
      isValid = false;
    } else if (formData.phone.length > 20) {
      toastr.error('Enter a valid phone number');
      isValid = false;
    } else {
      $('#phone').removeClass('is-invalid');
    }

    // Check if message is not empty  
    if (formData.message === "") {
      // toastr.error('Message is required');
      $('#message').addClass('is-invalid');
      isValid = false;
